public class Student {
    private String name;
    private int roll_no;
    private int age;
    private int score;
    public void setName(String nam)
    {
        name = nam;
    }    
    public String getName()
    {
        return name;
    }
    public void setRoll_no(int a)
    {
        roll_no = a;
    }    
    public int getRoll_no()
    {
        return roll_no;
    }
    public void setAge(int b)
    {
        age = b;
    }    
    public int getAge()
    {
        return age;
    }
    public void setScore(int s)
    {
        score = s;
    }    
    public int getScore()
    {
        return score;
    }
}
